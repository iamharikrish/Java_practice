package HackerRank;

public class subString {

	public static void main(String[] args) {
		
		String sub = "AdapaSai";
		
		int start = 2;
		int end = 4;
		
		for(int i=start;i<=end;i++) {
			System.out.print(sub.charAt(i));
			//Here it's printing the characters from that index values.
		}
		

	}

}
